      PROGRAM AVERAGE
      INTEGER A, B, C
      REAL MEAN
      
      PRINT *, "ENTER THE FIRST INTEGER"
      READ *, A
      
      PRINT *, "ENTER THE SECOND INTEGER"
      READ *, B
      
      PRINT *, "ENTER THE THIRD INTEGER"
      READ *, C
      
      MEAN = (A + B + C) / 3.0
      
      PRINT *, "FIRST = ",A
      PRINT *, "SECOND = ",B
      PRINT *, "THIRD = ",C
      PRINT *, "MEAN = ", MEAN
      

      END PROGRAM
